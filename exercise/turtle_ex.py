import turtle
def turtle_house():
    turtle.Turtle()
    turtle.forward(200)
    turtle.seth(120)
    turtle.forward(200)
    turtle.seth(240)
    turtle.forward(200)
    turtle.seth(270)
    turtle.forward(200)
    turtle.seth(0)
    turtle.forward(200)
    turtle.seth(90)
    turtle.forward(200)

    turtle.done()
if __name__ == "__main__":
    turtle_house()
